package it.unibs.fp.mylib;

public class ConfrontoDati {

	public static int maggioreInteri(int n1, int n2) {
		
		if( n1 > n2 ) {
			
			return n1;
		}
		else {
			
			return n2;
		}
	}
	
	public static int minoreInteri(int n1, int n2) {
		
		if( n1 < n2 ) {
			
			return n1;
		}
		else {
			
			return n2;
		}
	}
	
	public static double maggioreDouble(double n1, double n2) {
		
		if( n1 > n2 ) {
			
			return n1;
		}
		else {
			
			return n2;
		}
	}
	
	public static double minoreDouble(double n1, double n2) {
		
		if( n1 < n2 ) {
			
			return n1;
		}
		else {
			
			return n2;
		}
	}
	
}
